import type { OpportunityCard } from '../types/game';

function stock(
  id: string,
  name: string,
  ticker: string,
  exchange: string,
  sector: string,
  price: number,
  shares: number,
  dividendYield: number,
  peTTM: number,
  pb: number,
  kind: 'smallDeal' | 'bigDeal',
  extra?: Partial<OpportunityCard>
): OpportunityCard {
  const cost = price * shares;
  const monthlyDividend = Math.round((cost * dividendYield) / 12);
  return {
    id,
    title: `${name} (${ticker})`,
    description: `${exchange}上市${sector}龙头，股息率约 ${(dividendYield * 100).toFixed(1)}%。`,
    type: 'opportunity',
    kind,
    asset: {
      id: `${id}_asset`,
      name,
      type: 'stock',
      cost,
      downPayment: cost,
      cashFlow: monthlyDividend,
      mortgage: 0,
      marketValue: cost,
      shares,
      metadata: {
        sector,
        ticker,
        exchange,
        liquidity: 'T+1',
        incomeType: 'dividend',
        riskLevel: 'medium',
        subCategory: '蓝筹股',
        peTTM,
        pb,
        dividendYield,
        minInvestment: cost,
      },
    },
    ...extra,
  };
}

